package org.JavaCar;

public class Roda {

    // Variables
    private String marca;
    private double diametre;

    // Constructor per inicialitzar la roda
    public Roda(String marca, double diametre) {
        this.marca = marca;
        this.diametre = diametre;
    }

    // Getter de la Marca
    public String getMarca() {
        return marca;
    }

    // Getter del Diametre
    public double getDiametre() {
        return diametre;
    }


}

